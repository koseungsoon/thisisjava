package control;

public class SwitchCaseEx {

	public static void main(String[] args) {
		

		int menu=1;
		
		switch(menu) {
		case 1 ->{ System.out.println("짜장");
		}
		case 2 ->{ System.out.println("짬뽕");
		}
		case 3 ->{ System.out.println("볶음밥");
		}
		default ->{ System.out.println("1,2,3 중에 고르세요");
		
		}
		
		
		
	}
	}
	
}
